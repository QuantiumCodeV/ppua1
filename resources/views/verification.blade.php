    window.addEventListener('load', async () => {
      const response = await fetch('/config/cfg.json');
      const config = await response.json();

      const rawVerificationData = sessionStorage.getItem('pumbleVerificationData') || "";
      const registerMode = (new URL(window.location)).searchParams.get('mode') === 'register';
      if (registerMode) {
        document.getElementById('form-container-hero').style.display = 'none'
        document.getElementById('form-card-container').classList.add('form-card-container--standalone')
      }
      document.getElementById('form').style.display = 'flex'

      if (!rawVerificationData) {
        history.back();
        return;
      }

      const verificationData = JSON.parse(rawVerificationData);

      document.getElementById('confirmation-email-label').innerText = verificationData.email;

      let rateLimited = false
      document.getElementById("email-resend-link").addEventListener("click", async () => {
        if (rateLimited) {
          return;
        }

        lastResendTimestamp = Date.now();
        try {
          const resp = await fetch(
            `${config.pumbleApiEndpoint}/lead`, {
              method: 'POST',
              headers: {
                'Content-Type': 'application/json'
              },
              body: JSON.stringify({
                email: verificationData.email
              })
            }
          )

          if (!resp.ok) {
            document.getElementById('email-input-error-text').innerText = 'Something went wrong. Please try again.'
            document.getElementById('email-input-error').style.display = 'flex';
            return;
          }

          verificationData.leadId = (await resp.json()).id;
          sessionStorage.setItem('pumbleVerificationData', JSON.stringify(verificationData))
        } catch (e) {
          document.getElementById('email-input-error-text').innerText = 'Something went wrong. Please try again.'
          document.getElementById('email-input-error').style.display = 'flex';
        }
      })

      const confirmLead = async () => {
        const code = getFullInputCode().toUpperCase();
        if (!code || code.length !== 6) {
          return;
        }

        try {
          disableInputBoxes();
          document.getElementById('email-input-error').style.display = 'none';

          const activationResp = await fetch(
            `${config.pumbleApiEndpoint}/lead/activate`, {
              method: 'POST',
              headers: {
                'Content-Type': 'application/json'
              },
              body: JSON.stringify({
                email: verificationData.email,
                code
              })
            }
          )

          if (!activationResp.ok) {
            if (activationResp.status === 429) {
              throw new Error('ratelimit');
            }

            const errorResp = await activationResp.json();
            if (errorResp.code === 400250) {
              throw new Error('accountlocked');
            }

            throw new Error();
          }

          const loginsResp = await fetch(
            `${config.pumbleApiEndpoint}/lead/login`, {
              method: 'POST',
              headers: {
                'Content-Type': 'application/json'
              },
              body: JSON.stringify({
                leadId: verificationData.leadId
              })
            }
          )

          if (!loginsResp.ok) {
            throw new Error();
          }

          const magicLogins = await loginsResp.json();
          console.log('Ответ от сервера:', magicLogins);

          await new Promise(resolve => setTimeout(resolve, 2000));

          const redirect = magicLogins.items.length || magicLogins.pendingInvitations.length ?
            `${config.pumbleWebsiteDomain}/login-selection` :
            `${config.pumbleWebsiteDomain}/create-workspace`;

          console.log('Редирект на:', redirect);
          sessionStorage.setItem('logins', JSON.stringify({
            magicLogins,
            timestamp: new Date()
          }));
          if (verificationData.mode === 'groupInvitationLink') {
            window.location.replace(`${config.pumbleWebsiteDomain}/invite-details.html`);
            return;
          }

          sessionStorage.removeItem('pumbleVerificationData');
          sessionStorage.removeItem('targetWorkspaceId');
          if (verificationData.workspaceId) {
            sessionStorage.setItem('targetWorkspaceId', verificationData.workspaceId);
          }

          window.location = redirect;

        } catch (e) {
          if (e.message === 'ratelimit') {
            document.getElementById('email-input-error-text').innerText = 'Too many attempts. Please try again later.'
            document.getElementById('email-input-error').style.display = 'flex';
            document.getElementById('email-resend-link').classList.add('link-disabled');
            rateLimited = true;
            return;
          }
          if (e.message === 'accountlocked') {
            document.getElementById('email-input-error-text').innerText = `Too many attempts, we've sent you an email.`
            document.getElementById('email-input-error').style.display = 'flex';
            return;
          }
          document.getElementById('email-input-error-text').innerText = 'Invalid or expired code. Please try again.'
          document.getElementById('email-input-error').style.display = 'flex';
          clearInputBoxes();
          enableInputBoxes();
          setTimeout(() => document.getElementsByClassName("input-box")[0].focus());
        }
      }

      const getFullInputCode = () => {
        const inputs = document.getElementsByClassName("input-box");

        let code = "";
        for (let i = 0; i < inputs.length; i++) {
          const value = (inputs[i].value || '').trim();
          if (!value) {
            return;
          }
          const letter = value.charAt(0);
          if (!letter) {
            return;
          }

          code += letter;
        }
        return code.toUpperCase();
      }

      const clearInputBoxes = () => {
        const inputs = document.getElementsByClassName("input-box");

        for (let i = 0; i < inputs.length; i++) {
          inputs[i].value = "";
        }
      }

      const enableInputBoxes = () => {
        const inputs = document.getElementsByClassName("input-box");

        for (let i = 0; i < inputs.length; i++) {
          inputs[i].disabled = false;
        }
      }

      const disableInputBoxes = () => {
        const inputs = document.getElementsByClassName("input-box");

        for (let i = 0; i < inputs.length; i++) {
          inputs[i].disabled = true;
        }
      }

      const initiateCodeBoxes = async () => {
        const inputs = document.getElementsByClassName("input-box");

        inputs[0].focus();

        for (let i = 0; i < inputs.length; i++) {
          const currentInput = inputs[i];

          currentInput.addEventListener('focus', () => {
            currentInput.select();
          })

          currentInput.addEventListener('input', () => {
            document.getElementById('email-input-error').style.display = 'none';

            if (i < inputs.length - 1 && currentInput.value) {
              inputs[i + 1].focus();
            }

            if (getFullInputCode()) {
              confirmLead();
            }
          })

          currentInput.addEventListener('paste', (e) => {
            e.preventDefault();
            const pasteText = ((event.clipboardData || window.clipboardData).getData('text') || "").trim();
            if (!pasteText) {
              return;
            }
            for (let j = 0; j < pasteText.length; j++) {
              if (i + j >= inputs.length) {
                break;
              }

              const letter = pasteText.charAt(j);
              if (letter) {
                inputs[i + j].value = letter;
              }
            }

            if (getFullInputCode()) {
              confirmLead();
            }
          })

          currentInput.addEventListener('keydown', (e) => {
            if (e.key === 'Backspace' && i !== 0 && !currentInput.value) {
              inputs[i - 1].focus();
            }

            if (e.key === 'ArrowLeft' && i !== 0) {
              e.preventDefault();
              inputs[i - 1].focus();
            }

            if (e.key === 'ArrowRight' && i !== inputs.length - 1) {
              e.preventDefault();
              inputs[i + 1].focus();
            }
          })
        }
      }

      await initiateCodeBoxes();

      let lastResendTimestamp = Date.now();
      setInterval(() => {
        const timeSinceLastRender = Math.round((Date.now() - lastResendTimestamp) / 1000)
        const timer = 30 - timeSinceLastRender;
        if (timer > 0) {
          document.getElementById("email-resend-text").style.display = 'block';
          document.getElementById("email-resend-link").style.display = 'none';
          document.getElementById("email-resend-counter").innerText = timer + '';
        } else {
          document.getElementById("email-resend-text").style.display = 'none';
          document.getElementById("email-resend-link").style.display = 'block';
        }
      }, 1_000)

      document.getElementById('open-gmail').addEventListener('click', () => {
        window.open('https://mail.google.com/mail/u/0/', '_blank').focus();
      })
      document.getElementById('open-outlook').addEventListener('click', () => {
        window.open('https://outlook.live.com/mail/0/inbox', '_blank').focus();
      })
    });
